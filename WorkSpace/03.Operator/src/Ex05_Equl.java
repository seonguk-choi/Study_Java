
public class Ex05_Equl {
	public static void main(String[] args) {
		int result = 0; //0으로 초기화
						//results는 main method 안에서 재사용 가능
		//+= , -=
		result += 10; // result(0) = result + 10;
		System.out.println(result);
		result -= 4;   // result(10) = result - 4;
		System.out.println(result);
		result *= 2;
		System.out.println(result);
		result /= 3;
		System.out.println(result);
		result %= 3;
		System.out.println(result);
		
	}
}
