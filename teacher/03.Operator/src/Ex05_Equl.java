
public class Ex05_Equl {
	public static void main(String[] args) {
		int result = 0; //0으로 초기화 
						//result라는 변수는 main method 지역안에서 사용할수있다.
		// 사칙연산기호  + =
		// += , -= , *= , /=
		result += 10; //result(0) = result(0) + 10;
		System.out.println(result);
		result -= 3;  //result = result(10) - 3 ;
		System.out.println(result);
		result *= 2;
		System.out.println(result);
		result /= 3;
		System.out.println(result);
		
		
		
		
		
		
		
		
		
	
	}
}
